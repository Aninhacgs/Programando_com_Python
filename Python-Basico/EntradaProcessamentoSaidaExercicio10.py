#11) Escreva um algoritmo em PYTHON que leia um número inteiro e imprima o seu sucessor e seu antecessor.


#Entrada do valor

valor = int(input("Digite um valor: "))

#Processamento do valor

sucessor = valor + 1

antecessor = valor - 1

#Impressão dos dados

print("Antecessor: ", antecessor, "\nSucessor: ",sucessor)
