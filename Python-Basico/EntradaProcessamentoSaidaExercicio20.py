#Criar um algoritmo em PYTHON para calcular e apresentar o valor do volume de uma lata de óleo, utilizando a fórmula:
#V = 3.14159 * R ** 2 * H onde V é o volume, R é o raio e H é a altura.

#Entrada de dados

R = float(input("Digite o valor correspondente ao raio: "))

H = float(input("Digite o valor correspondente a altura: "))

#Processamento dos dados

V = 3.14159 * R ** 2 * H

#Impressão dos dados

print("Volume da Lata de óleo: ", V)
